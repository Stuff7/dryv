const fn mb_info<const N: usize>(elements: &'static [(u8, &[u8])]) -> [&'static [u8]; N] {
  let mut arr: [&[u8]; N] = [&[]; N];
  let mut i = 0;

  while i < elements.len() {
    let (index, value) = elements[i];
    arr[index as usize] = value;
    i += 1;
  }

  arr
}

